// Set
// Definition:
// Collection of unique values (no duplicates allowed).

// Detailed Explanation:

// javascript
// const set = new Set([1, 2, 3, 3, 4]); // {1, 2, 3, 4}
// set.add(5);
// set.delete(1);
// Key Features:

// Stores unique values of any type

// Maintains insertion order

// Simple API for value management

// Methods:

// add() - Adds value

// has() - Checks for value

// delete() - Removes value

// clear() - Removes all values

// Conversion:

// Array.from(set) - Convert to array

// new Set(array) - Remove duplicates from array

// Use Cases:

// Removing duplicates from arrays

// Tracking unique items

// Mathematical set operations