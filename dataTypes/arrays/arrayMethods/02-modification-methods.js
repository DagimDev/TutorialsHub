push()
// ➤ What it does:
// Adds one or more elements to the end of an array.

// ➤ Syntax:

// array.push(element1, element2, ...);
// ➤ Returns:
// The new length of the array.

// ➤ Mutates original array? ✅ Yes
// ➤ Example:

let colors = ['red', 'green'];
colors.push('blue');

console.log(colors); // ['red', 'green', 'blue']